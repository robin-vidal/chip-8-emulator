package chip8

import (
	"fmt"
	"os"
)

const (
	memorySize   = 4096
	programStart = 0x200
	fontStart    = 0x050
)

type VM struct {
	memory  [memorySize]uint8
	display [ScreenWidth * ScreenHeight]bool
	pc      uint16
	i       uint16
	stack   []uint16
	delay   uint8
	sound   uint8
	v       [16]uint8
}

func New() *VM {
	font := [80]uint8{
		0x0F0, 0x090, 0x090, 0x090, 0x0F0, // 0
		0x020, 0x060, 0x020, 0x020, 0x070, // 1
		0x0F0, 0x010, 0x0F0, 0x080, 0x0F0, // 2
		0x0F0, 0x010, 0x0F0, 0x010, 0x0F0, // 3
		0x090, 0x090, 0x0F0, 0x010, 0x010, // 4
		0x0F0, 0x080, 0x0F0, 0x010, 0x0F0, // 5
		0x0F0, 0x080, 0x0F0, 0x090, 0x0F0, // 6
		0x0F0, 0x010, 0x020, 0x040, 0x040, // 7
		0x0F0, 0x090, 0x0F0, 0x090, 0x0F0, // 8
		0x0F0, 0x090, 0x0F0, 0x010, 0x0F0, // 9
		0x0F0, 0x090, 0x0F0, 0x090, 0x090, // A
		0x0E0, 0x090, 0x0E0, 0x090, 0x0E0, // B
		0x0F0, 0x080, 0x080, 0x080, 0x0F0, // C
		0x0E0, 0x090, 0x090, 0x090, 0x0E0, // D
		0x0F0, 0x080, 0x0F0, 0x080, 0x0F0, // E
		0x0F0, 0x080, 0x0F0, 0x080, 0x080, // F
	}

	vm := new(VM)
	vm.pc = programStart
	copy(vm.memory[fontStart:], font[:])

	return vm
}

func (vm *VM) Pixel(x, y int) bool {
	return vm.display[y*ScreenWidth+x]
}

func (vm *VM) LoadROM(path string) error {
	data, err := os.ReadFile(path)
	if err != nil {
		return err
	}

	if len(data) > memorySize-programStart {
		return fmt.Errorf("ROM too big")
	}

	copy(vm.memory[programStart:], data)

	return nil
}
